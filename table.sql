CREATE TABLE registration (
  id INT AUTO_INCREMENT PRIMARY KEY,
  firstname VARCHAR(255),
  lastname VARCHAR(255),
  email VARCHAR(255),
  mobile INT,
  password VARCHAR(255),
  user_type_id VARCHAR(255)
);

CREATE TABLE stall (
  id INT AUTO_INCREMENT PRIMARY KEY,
  firstname VARCHAR(255),
  lastname VARCHAR(255),
  email VARCHAR(255),
  mobile INT,
  password VARCHAR(255),
  user_type_id VARCHAR(255)
);

